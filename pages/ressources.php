        <h1>Ressources pédagogiques</h1> 
        <p>
            Dans cette section du site, nous expliquerons plus en détaillés la notion d’éveils aux langues, ces principes, spécificités et utilisations. Mais avant d’entrer dans le vif du sujet, il serait pertinent de comprendre ce que veut dire Approche plurielle, dans le contexte de l’enseignement et l'apprentissage linguistique des langues et des cultures. 
        </p>
        <h2 id="approches-plurielles">Que signifie le terme Approches pluriel ?</h2>
        <div id="ap-video">
            <iframe width="750" height="400" src="https://www.youtube.com/embed/_J0vmvSmls4" frameborder="0" allowfullscreen></iframe>
        </div>
        <p>
            Les approches plurielles, terme créé par le conseil de l’Europe, qui désigne la somme d’activités conçues dans le cadre de l’apprentissage des langues dans une perspective plurielle, en tenant compte de la diversité linguistique et culturelle. Les approches plurielles regroupent quatre notions qui sont des méthodologies de travail, bien que distincts, mais complémentaire, ils ouvrent à l’enseignement de la pluralité,  : 
        </p>
        <ol>  
            <li>Éveil aux langues</li>
            <li>Didactique intégrée</li>
            <li>Intercompréhension entre les langues parentes</li>
            <li>Approches interculturelles</li>
        </ol> 
        <p>
            Chaque approche à une place spécifique dans l’enseignement de la diversité. Chacun des approches offre aux enseignants et aux formateurs des axes de travail et des outils pédagogiques précis afin de développer chez l’apprenant de manière concrète des compétences fondamentales liées aux dimensions plurilingue et pluriculturel. 
        </p>
        <p>
            À ce sujet, le conseil de l’Europe conçoit le (CARAP) acronyme de Cadre de Référence pour les Approches Plurielles des Langues et des Cultures. Est un outil pédagogique et didactique essentiel à l’enseignement pluriel, il permet la mise en synergie entre les différentes approches plurielles, en mettant des stratégies d’apprentissage et des orientations didactiques afin qu’enseignant et formateur puisse concevoir des activités qui reflètent le principe d’éveils des approches plurielles. Afin de visualiser au mieux cette ressource, vous pouvez consulter le site officiel du conseil de l’Europe :  
        </p>
        <a id="carap-bouton" target="_blank" href="https://carap.ecml.at/Pluralisticapproaches/tabid/2681/language/fr-FR/Default.aspx#:~:text=Nous%20appelons%20%C2%AB%20Approches%20plurielles%20des,une)%20vari%C3%A9t%C3%A9s%20linguistiques%20et%20culturelles.">
            Voir le CARAP 
        </a>

        <h2 id="evl">C’est quoi l’éveil aux langues ?</h2>
        <p>
            « Les démarches d’éveil aux langues (dorénavant EVL) consistent à proposer aux élèves dans l’espace scolaire une approche de plusieurs langues (et par ce biais de plusieurs cultures) avec un travail transversal sur ces langues et la langue de l’école, » (Dompmartin, 2011). En effet, L'éveil aux langues (EVL), également connu sous le terme de sensibilisation linguistique, englobe un large éventail de processus, de méthodes et de pratiques visant à favoriser l'apprentissage et la maîtrise de plusieurs langues.
            <br><br>
            Pour ainsi dire, EVL est une approche plurielle qui reconnaît et encourage la diversité linguistique, tout en valorisant les compétences multilingues dans un monde de plus en plus interconnecté. Par ailleurs, cela permet aussi de décomplexifier le rapport à la langue maternelle et langue étrangère pour les enfants qui sont issus de familles où plusieurs systèmes linguistiques sont mobilisés et utilisés.
        </p>
        <h2 id="applications">Autre utilisation de l'éveil aux langues ?</h2>
        <p>
            Dans un article, Françoise Leclaire et Brigitte Lemattre, l’éveil aux langues est présenté comme médiation dans un contexte clinique à une fin thérapeutique pour des enfants migrants. Face à différents problèmes, notamment le mutisme sélectif, spectre autistique ou troubles relationnels, les ateliers d’éveils aux langues s’avèrent être une solution innovante, faisant le lien entre la langue de la famille et la langue de l’école et de la société. À travers un dispositif multimodal auprès des familles, les ateliers d’éveils aux langues cherchent à remanier l’environnement des enfants et à permettre de s’affranchir de cette situation linguistique favorable en incluant les acteurs (éducations et parents). 
